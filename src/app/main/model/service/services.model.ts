export class Services {
    constructor(
        public _id?: string,
        public serviceID?: string,
        public serviceName?: string,
    ) {
        if (this._id == null) { this._id = null; }
        if (this.serviceID == null) { this.serviceID = null; }
        if (this.serviceName == null) { this.serviceName = null; }
        if (this.serviceName == null) { this.serviceName = null; }
        if (this.serviceName == null) { this.serviceName = null; }
    }
}